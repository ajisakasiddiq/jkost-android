package com.example.jkost_android;

public class ModelClass {
    String kostName, kostNum;
    int img;

    public String getKostName() {
        return kostName;
    }

    public void setKostName(String kostName) {
        this.kostName = kostName;
    }

    public String getKostNum() {
        return kostNum;
    }

    public void setKostNum(String kostNum) {
        this.kostNum = kostNum;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
